const day = prompt('day');
switch (day) {
    case 'sunday':
        console.log("it's Sunday!");
        break;
    case 'monday':
        console.log("it's Monday!");
        break;
    case 'tuesday':
        console.log("it's Tuesday!");
        break;
    case 'wednesday':
        console.log("it's Wednesday!");
        break;
    case 'thrusday':
        console.log("it's Thrusday!");
        break;
    case 'friday':
        console.log("it's friday!");
        break;
    case 'saturday':
        console.log("it's Saturday!");
        break;

    default:
        console.log("It's not a fucki'n day!!!");
        break;
}